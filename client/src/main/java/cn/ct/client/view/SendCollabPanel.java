    private void sendCollabResult(ObjectWrapper data) {
        if (data.getMessage() == ObjectWrapper.MES_SUCCESS) {
            JOptionPane.showMessageDialog(this, "Send invitation successfully.");
            edtUsername.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Username is not exist.", 
                    "Message", JOptionPane.ERROR_MESSAGE);
        }
    }

