    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        if (edtUsername.getText().isBlank() || edtUsername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username can not empty.", 
                    "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            if (edtUsername.getText().equals(App.user.getUsername())) {
                JOptionPane.showMessageDialog(this, "You can not send invitaiton to myself.", 
                    "Message", JOptionPane.ERROR_MESSAGE);
            } else {
                App.ctrl.sendData(new ObjectWrapper(ObjectWrapper.GET_SEND_COLLAB, 
                            new Collab(0, new User(0, null, edtUsername.getText(), null), App.project, false)))
                        .register(ObjectWrapper.REPLY_SEND_COLLAB, data -> {
                    sendCollabResult(data);
                });
            }
        }
    }//GEN-LAST:event_btnSendActionPerformed


