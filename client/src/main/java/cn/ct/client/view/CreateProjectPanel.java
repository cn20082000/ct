    private void updateView() {
        edtOutput.setText(edtDir.getText() + "\\" + edtProj.getText());
        if (edtDir.getText().isBlank() || edtDir.getText().isEmpty() 
                || edtProj.getText().isBlank() || edtProj.getText().isEmpty()) {
            btnCreate.setEnabled(false);
            return;
        }
        if (new java.io.File(edtOutput.getText()).exists()) {
            tvMessage.setText("Folder \"" + edtProj.getText() + "\" is exists.");
            btnCreate.setEnabled(false);
        } else {
            tvMessage.setText("");
            btnCreate.setEnabled(true);
        }
    }
    
    private void createProjectResult(ObjectWrapper data) {
        if (data.getMessage() == ObjectWrapper.MES_SUCCESS) {
            JOptionPane.showMessageDialog(this, "Create project successfully.");
            parent.close();
        } else {
            JOptionPane.showMessageDialog(this, "Create project failed.", 
                    "Message", JOptionPane.ERROR_MESSAGE);
        }
    }

