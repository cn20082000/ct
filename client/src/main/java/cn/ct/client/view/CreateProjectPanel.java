    private void btnDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDirActionPerformed
        // TODO add your handling code here:
        JFileChooser f = new JFileChooser();
        if (!edtDir.getText().isEmpty()) {
            f.setCurrentDirectory(new java.io.File(edtDir.getText()));
        }
        f.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        f.showDialog(this, "Open");
        
        edtDir.setText(f.getSelectedFile().getAbsolutePath());
        updateView();
    }//GEN-LAST:event_btnDirActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        App.ctrl.sendData(new ObjectWrapper(ObjectWrapper.GET_CREATE_PROJECT, 
                    new Project(0, null, null, new Info(0, edtProj.getText(), null, null))))
                .register(ObjectWrapper.REPLY_CREATE_PROJECT, data -> {
            createProjectResult(data);
        });
    }//GEN-LAST:event_btnCreateActionPerformed

